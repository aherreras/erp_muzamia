    public void listarCategorias() {
        DaoCategorias daoCategorias = new DaoCategoriasImpl();
        List<Categorias> list = daoCategorias.lst_Categorias_3();

        if (list != null) {
            Object[] row;
            DefaultTableModel model = (DefaultTableModel) jtCategorias.getModel();
            model.setRowCount(0);

            for (int i = 0; i < list.size(); i++) {
                row = new Object[4];
                row[0] = list.get(i).getCate_id();
                row[1] = list.get(i).getCate_descripcion();

                if (list.get(i).isCate_estado()) {
                    row[2] = "Activo";
                } else {
                    row[2] = "Inactivo";
                }

                row[3] = list.get(i).getCate_timestamp();

                model.addRow(row);
            };
        }
    }

