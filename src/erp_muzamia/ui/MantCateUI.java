    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        String message = null;

        if (jtfCategoria.getText().isEmpty()) {
            message = "Validación de campos:\n";
            message += "\n* Ingresar Nombre Categoría.";
        }

        if (message != null) {
            JOptionPane.showMessageDialog(null, message, "Alerta", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        Categorias categorias = new Categorias();
        if (!jtfID.getText().isEmpty()) {
            categorias.setCate_id(Integer.parseInt(jtfID.getText()));
        }

        categorias.setCate_descripcion(jtfCategoria.getText());

        int estado = jcbEstado.getSelectedIndex();
        if (estado == 0) {
            categorias.setCate_estado(true);
        } else {
            categorias.setCate_estado(false);
        }

        message = null;

        DaoCategorias daoCategorias = new DaoCategoriasImpl();
        if (accion == 0) {
            message = daoCategorias.ins_Categoria(categorias);
        } else {
            message = daoCategorias.upd_Categoria(categorias);
        }

        if (message != null) {
            JOptionPane.showMessageDialog(null, message, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String cad = null;
        if (accion == 0) {
            cad = "Categoría registrado satisfactoriamente.";
        } else {
            cad = "Categoría actualizado satisfactoriamente.";
        }
        
        JOptionPane.showMessageDialog(null, cad, "En hora buena!", JOptionPane.INFORMATION_MESSAGE);

        limpiar_datos();
        habilita_campos(false);

        jbNuevo.setEnabled(true);
        jbLimpiar.setEnabled(false);
        jbModificar.setEnabled(false);
        jbGuardar.setEnabled(false);
        jbEliminar.setEnabled(false);

        listarCategorias();
    }//GEN-LAST:event_jbGuardarActionPerformed

