    private void jtCategoriasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtCategoriasMouseClicked
        int row = jtCategorias.getSelectedRow();
        jtfID.setText(jtCategorias.getValueAt(row, 0).toString());
        jtfCategoria.setText(jtCategorias.getValueAt(row, 1).toString());

        if (jtCategorias.getValueAt(row, 2) == "Activo") {
            jcbEstado.setSelectedIndex(0);
        } else if (jtCategorias.getValueAt(row, 2) == "Inactivo") {
            jcbEstado.setSelectedIndex(1);
        }

        jbNuevo.setEnabled(true);
        jbLimpiar.setEnabled(false);
        jbModificar.setEnabled(true);
        jbGuardar.setEnabled(false);
        jbEliminar.setEnabled(true);

        jtfCategoria.setEnabled(false);
        jcbEstado.setEnabled(false);
    }//GEN-LAST:event_jtCategoriasMouseClicked

